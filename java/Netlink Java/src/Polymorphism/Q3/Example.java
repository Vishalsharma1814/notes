package Polymorphism.Q3;

public class Example {

    public void h1(int a){
        System.out.println("Integer value: "+a);
    }
    public void h1(float b, int c,char j){
        System.out.println("Float value: "+b+" "+"Integer value: "+c+" "+"Character value: "+j);
    }
    public void h1(int f,char k,double h,long u){
        System.out.println("Integer value: "+f+" "+"Character value: "+k+" "+"Double value: "+h+" "+"Long value: "+u);
    }
    public void h1(int k,float n){
        System.out.println("Integer value: "+k+" "+"Float value: "+n);
    }
}
